    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if(idSeleccionado == 0){
        JOptionPane.showMessageDialog(this, "Selecciona un registro");
        return;
    }

    try {
        Connection con = Conexion.conectar();

        PreparedStatement ps = con.prepareStatement(
            "UPDATE peliculas SET titulo=?, anio=?, duracion=? WHERE id_pelicula=?"
        );

        ps.setString(1, txtTitulo.getText());
        ps.setInt(2, Integer.parseInt(txtAnio.getText()));
        ps.setInt(3, Integer.parseInt(txtDuracion.getText()));
        ps.setInt(4, idSeleccionado);

        ps.executeUpdate();

        JOptionPane.showMessageDialog(this, "Actualizado correctamente");

        limpiar();
        cargarTabla();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e);
    }
    }//GEN-LAST:event_btnEditarActionPerformed

