    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // 🔹 VALIDACIÓN BÁSICA
    if(txtTitulo.getText().isEmpty() || txtAnio.getText().isEmpty() || txtDuracion.getText().isEmpty()){
        JOptionPane.showMessageDialog(this, "Llena todos los campos");
        return;
    }

    if(fis == null){
        JOptionPane.showMessageDialog(this, "Selecciona una imagen");
        return;
    }

    try {
        Connection con = Conexion.conectar();

        PreparedStatement ps = con.prepareStatement(
            "INSERT INTO peliculas (titulo,id_genero,id_director,anio,duracion,clasificacion,portada) VALUES (?,?,?,?,?,?,?)"
        );

        ps.setString(1, txtTitulo.getText());
        
        modelo.ComboItem genero = (modelo.ComboItem) cbGenero.getSelectedItem();
modelo.ComboItem director = (modelo.ComboItem) cbDirector.getSelectedItem();

ps.setInt(2, genero.getId());
ps.setInt(3, director.getId());;

        ps.setInt(4, Integer.parseInt(txtAnio.getText()));
        ps.setInt(5, Integer.parseInt(txtDuracion.getText()));
        ps.setString(6, cbClasificacion.getSelectedItem().toString());
        ps.setBinaryStream(7, fis, longitudBytes);

        ps.executeUpdate();

        JOptionPane.showMessageDialog(this, "Película guardada correctamente");

        limpiar();
        cargarTabla();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e);
    }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnImagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImagenActionPerformed

        try {

    JFileChooser file =
            new JFileChooser();

    int opcion =
            file.showOpenDialog(this);

    if(opcion == JFileChooser.APPROVE_OPTION){

        File archivo =
                file.getSelectedFile();

        BufferedImage bufferedImage =
        ImageIO.read(archivo);

if(bufferedImage == null){

    JOptionPane.showMessageDialog(this,

        "Formato de imagen no compatible.\n" +

        "Use JPG o PNG reales."
    );

    return;
}

        Image imgEscalada =
                bufferedImage.getScaledInstance(

                    lblImagen.getWidth(),
                    lblImagen.getHeight(),

                    Image.SCALE_SMOOTH
                );

        lblImagen.setIcon(

            new ImageIcon(imgEscalada)
        );

        fis = new FileInputStream(archivo);

        longitudBytes =
                (int) archivo.length();
    }

} catch (Exception e) {

    JOptionPane.showMessageDialog(this,

            "Error al cargar imagen: "
            + e);
}
    }//GEN-LAST:event_btnImagenActionPerformed

    private void tablaPeliculasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaPeliculasMouseClicked
        int fila = tablaPeliculas.getSelectedRow();

    if(fila >= 0){
        idSeleccionado = Integer.parseInt(tablaPeliculas.getValueAt(fila, 0).toString());

        txtTitulo.setText(tablaPeliculas.getValueAt(fila, 1).toString());
        txtAnio.setText(tablaPeliculas.getValueAt(fila, 2).toString());
        txtDuracion.setText(tablaPeliculas.getValueAt(fila, 3).toString());

        // 🔥 TRAER IMAGEN DESDE BD
        try {
            Connection con = Conexion.conectar();

            PreparedStatement ps = con.prepareStatement(
                "SELECT portada FROM peliculas WHERE id_pelicula=?"
            );

            ps.setInt(1, idSeleccionado);

            ResultSet rs = ps.executeQuery();

            if(rs.next()){
                byte[] imagenBytes = rs.getBytes("portada");

                if(imagenBytes != null){
                    Image img = new ImageIcon(imagenBytes).getImage();
                    Image imgEscalada = img.getScaledInstance(
                            lblImagen.getWidth(),
                            lblImagen.getHeight(),
                            Image.SCALE_SMOOTH
                    );

                    lblImagen.setIcon(new ImageIcon(imgEscalada));
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al mostrar imagen");
        }
    }
    }//GEN-LAST:event_tablaPeliculasMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if(idSeleccionado == 0){
        JOptionPane.showMessageDialog(this, "Selecciona un registro");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(this, "¿Seguro que deseas eliminar?");

    if(confirm == 0){
        try {
            Connection con = Conexion.conectar();

            PreparedStatement ps = con.prepareStatement(
                "DELETE FROM peliculas WHERE id_pelicula=?"
            );

            ps.setInt(1, idSeleccionado);
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "Eliminado");

            limpiar();
            cargarTabla();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e);
        }
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

