    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
       try {

        ComboItem pelicula =
                (ComboItem) cbPelicula.getSelectedItem();

        Connection con = Conexion.conectar();

        PreparedStatement ps = con.prepareStatement(

            "INSERT INTO resenas " +

            "(id_pelicula, usuario, calificacion, comentario, fecha) " +

            "VALUES (?,?,?,?,?)"
        );

        ps.setInt(1, pelicula.getId());

        ps.setString(2, usuario);

        ps.setInt(3,
    cbCalificacion.getSelectedIndex() + 1
        );

        ps.setString(4,
                txtComentario.getText()
        );

        java.sql.Date fechaActual =
                new java.sql.Date(
                        System.currentTimeMillis()
                );

        ps.setDate(5, fechaActual);

        ps.executeUpdate();

        JOptionPane.showMessageDialog(this,
                "Reseña agregada");

        limpiar();

        cargarTabla();

    } catch (Exception e) {

        JOptionPane.showMessageDialog(this,
                "Error: " + e);
    }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void tablaResenasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaResenasMouseClicked
        int fila = tablaResenas.getSelectedRow();

    idSeleccionado = Integer.parseInt(
            tablaResenas.getValueAt(fila, 0).toString()
    );

    usuarioResena =
            tablaResenas.getValueAt(fila, 2).toString();

    // comentario
    txtComentario.setText(
            tablaResenas.getValueAt(fila, 4).toString()
    );

    // calificación
    int calificacion =
            Integer.parseInt(
                    tablaResenas.getValueAt(fila, 3).toString()
            );

    cbCalificacion.setSelectedIndex(calificacion - 1);

    // película
    String peliculaTabla =
            tablaResenas.getValueAt(fila, 1).toString();

    for(int i = 0; i < cbPelicula.getItemCount(); i++){

        ComboItem item = cbPelicula.getItemAt(i);

        if(item.toString().equals(peliculaTabla)){

            cbPelicula.setSelectedIndex(i);

            break;
        }
    }

    // actualizar promedio
    calcularPromedio();

    verificarPermisoEliminar();
    }//GEN-LAST:event_tablaResenasMouseClicked

