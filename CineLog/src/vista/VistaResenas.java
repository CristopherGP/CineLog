    private void configurarRoles(){

    if(rol.equals("consultor")){

        // Puede agregar
        btnAgregar.setEnabled(true);

        // Eliminar depende de la reseña seleccionada
        btnEliminar.setEnabled(false);

    } else {

        // admin y operador solo ven

        btnAgregar.setEnabled(false);
        btnEliminar.setEnabled(false);
    }
}
    
    private void verificarPermisoEliminar(){

    if(rol.equals("consultor")){

        if(usuario.equals(usuarioResena)){

            btnEliminar.setEnabled(true);

        } else {

            btnEliminar.setEnabled(false);
        }
    }
}
    
    private void calcularPromedio(){

    try {

        ComboItem pelicula =
                (ComboItem) cbPelicula.getSelectedItem();

        Connection con = Conexion.conectar();

        PreparedStatement ps = con.prepareStatement(

            "SELECT AVG(calificacion) promedio " +
            "FROM resenas WHERE id_pelicula=?"
        );

        ps.setInt(1, pelicula.getId());

        ResultSet rs = ps.executeQuery();

        if(rs.next()){

            double promedio =
                    rs.getDouble("promedio");

            lblPromedio.setText(
                    "Promedio: " +
                    String.format("%.1f", promedio)
            );
        }

    } catch (Exception e) {

        lblPromedio.setText("Promedio: 0");
    }
}
