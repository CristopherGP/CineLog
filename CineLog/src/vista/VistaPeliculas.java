private void cargarTabla(){

    DefaultTableModel modelo =
            new DefaultTableModel();

    modelo.addColumn("ID");
    modelo.addColumn("Título");
    modelo.addColumn("Año");
    modelo.addColumn("Duración");
    modelo.addColumn("Género");
    modelo.addColumn("Director");

    tablaPeliculas.setModel(modelo);

    try {

        Connection con =
                Conexion.conectar();

        Statement st =
                con.createStatement();

        ResultSet rs =
                st.executeQuery(

            "SELECT * FROM vista_peliculas"
        );

        while(rs.next()){

            Object[] fila =
                    new Object[6];

            fila[0] =
                    rs.getInt("id_pelicula");

            fila[1] =
                    rs.getString("titulo");

            fila[2] =
                    rs.getInt("anio");

            fila[3] =
                    rs.getInt("duracion");

            fila[4] =
                    rs.getString("genero");

            fila[5] =
                    rs.getString("director");

            modelo.addRow(fila);
        }

    } catch (Exception e) {

        JOptionPane.showMessageDialog(this,

                "Error al cargar tabla:\n"
                + e);
    }

    sorter =
        new TableRowSorter<>(
            (DefaultTableModel)
            tablaPeliculas.getModel()
        );

    tablaPeliculas.setRowSorter(sorter);
}
private void cargarGeneros(){

    try {
        Connection con = Conexion.conectar();
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery("SELECT * FROM generos");

        cbGenero.removeAllItems();

        while(rs.next()){
            cbGenero.addItem(new modelo.ComboItem(
                rs.getInt("id_genero"),
                rs.getString("nombre")
            ));
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al cargar géneros");
    }
}
private void cargarDirectores(){

    try {
        Connection con = Conexion.conectar();
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery("SELECT * FROM directores");

        cbDirector.removeAllItems();

        while(rs.next()){
            cbDirector.addItem(new modelo.ComboItem(
                rs.getInt("id_director"),
                rs.getString("nombre")
            ));
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al cargar directores");
    }
}
