    private void cargarPeliculas(){

    try {

        Connection con = Conexion.conectar();

        Statement st = con.createStatement();

        ResultSet rs = st.executeQuery(
            "SELECT * FROM peliculas"
        );

        cbPelicula.removeAllItems();

        while(rs.next()){

            cbPelicula.addItem(
                new ComboItem(
                    rs.getInt("id_pelicula"),
                    rs.getString("titulo")
                )
            );
        }

    } catch (Exception e) {

        JOptionPane.showMessageDialog(this,
                "Error al cargar películas");
    }
}
    
    private void cargarTabla(){

    DefaultTableModel modelo = new DefaultTableModel();

    modelo.addColumn("ID");
    modelo.addColumn("Película");
    modelo.addColumn("Usuario");
    modelo.addColumn("Calificación");
    modelo.addColumn("Comentario");
    modelo.addColumn("Fecha");

    tablaResenas.setModel(modelo);

    try {

        Connection con = Conexion.conectar();

        Statement st = con.createStatement();

        ResultSet rs = st.executeQuery(

            "SELECT * FROM vista_resenas"
        );

        while(rs.next()){

            Object[] fila = new Object[6];

            fila[0] = rs.getInt("id_resena");
            fila[1] = rs.getString("titulo");
            fila[2] = rs.getString("usuario");
            fila[3] = rs.getInt("calificacion");
            fila[4] = rs.getString("comentario");
            fila[5] = rs.getDate("fecha");

            modelo.addRow(fila);
        }

    } catch (Exception e) {

        JOptionPane.showMessageDialog(this,
                "Error al cargar reseñas");
    }
}
    
